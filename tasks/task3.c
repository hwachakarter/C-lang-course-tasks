#include <stdio.h>

int main() {
    int n;
    // Считывание числа
    printf("Введите число n: ");
    scanf("%d", &n);

    // Вывод числа и пробела
    printf("%d ", n);

    // Определение правильной формы слова
    if (n % 10 == 1 && n % 100 != 11) {
        printf("год\n"); // Например: 1 год, 21 год, 31 год
    } else if ((n % 10 >= 2 && n % 10 <= 4) && !(n % 100 >= 12 && n % 100 <= 14)) {
        printf("года\n"); // Например: 2 года, 3 года, 4 года, 22 года
    } else {
        printf("лет\n"); // Например: 5 лет, 11 лет, 14 лет, 25 лет
    }

    return 0;
}